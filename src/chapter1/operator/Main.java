package chapter1.operator;

public class Main {
    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        // 기본적인 사칙연산
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));

    }
}
